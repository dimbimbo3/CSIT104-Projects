x1 = float(input("Enter the x-coordiante for point1:"))
y1 = float(input("Enter the y-coordiante for point1:"))
x2 = float(input("Enter the x-coordiante for point2:"))
y2 = float(input("Enter the y-coordiante for point2:"))
slope = (y2 - y1)/(x2 - x1)
print("The slope for the line that connects two points(",x1,", ",y1,
      ") and (",x2,", ",y2,") is", slope)
